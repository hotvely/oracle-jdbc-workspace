-----------------도서 테이블 생성
CREATE TABLE TB_BOOK
(
    BK_NO NUMBER PRIMARY KEY ,  
    BK_TITLE VARCHAR2(100) NOT NULL,
    BK_AUTHOR VARCHAR2(50) NOT NULL,
    BK_PRICE VARCHAR2(10),
    BK_PUB_NO NUMBER REFERENCES TB_PUBLISHER ON DELETE CASCADE
