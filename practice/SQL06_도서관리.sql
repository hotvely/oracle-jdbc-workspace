CREATE TABLE TB_RENT
(
RENT_NO NUMBER PRIMARY KEY,--(대여번호) -- 기본 키
RENT_MEM_NO NUMBER REFERENCES TB_MEMEBER ON DELETE CASCADE,--(대여 회원번호) 
-- 외래 키(TB_MEMBER와 참조)이때 부모 데이터 삭제 시 NULL 값이 되도록 옵션 설정
RENT_BOOK_NO NUMBER REFERENCES TB_BOOK ON DELETE CASCADE,--(대여 도서번호) 
-- 외래 키(TB_BOOK와 참조) 이때 부모 데이터 삭제 시 NULL 값이 되도록 옵션 설정
RENT_DATE DATE DEFAULT SYSDATE --(대여일) -- 기본값 SYSDATE
